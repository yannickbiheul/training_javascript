// BOUCLE WHILE
let compteur = 0;
while (compteur < 3) {
    console.log("Compteur : " + compteur);
    console.log("Instruction de la boucle while");
    compteur++;
}
console.log("Fin de la boucle while");

// BOUCLE DO WHILE
let compteur2 = 0;
do {
    console.log("Compteur : " + compteur2);
    console.log("Instruction de la boucle do while");
    compteur2++;
} while (compteur2 < 3);
console.log("Fin de la boucle do while");

// BOUCLE FOR
for (let compteur3 = 0; compteur3 < 3; compteur3++) {
    console.log("Compteur : " + compteur3);
    console.log("Instruction de la boucle for");
}
console.log("Fin de la boucle for");